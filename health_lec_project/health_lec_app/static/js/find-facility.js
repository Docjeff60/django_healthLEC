function filterHospitals() {
  console.log("Filter function called");
  const facilityType = document.getElementById('facilityType').value;
  const distance = document.getElementById('distance').value;
  const state = document.getElementById('state').value;
  const city = document.getElementById('city').value;
  const ratings = document.getElementById('ratings').value;
  const resultsContainer = document.getElementById('results');
  resultsContainer.innerHTML = '';

  console.log({ facilityType, distance, state, city, ratings });

  const filteredHospitals = hospitalsInNigeria.filter(hospital => {
      let isMatch = true;

      if (facilityType && hospital.type !== facilityType) {
          isMatch = false;
      }
      if (distance) {
          const [min, max] = distance.split('-').map(Number);
          if (hospital.distance < min || hospital.distance > max) {
              isMatch = false;
          }
      }
      if (state && hospital.state !== state) {
          isMatch = false;
      }
      if (city && hospital.city !== city) {
          isMatch = false;
      }
      if (ratings && hospital.ratings < ratings) {
          isMatch = false;
      }

      return isMatch;
  });

  console.log("Filtered Hospitals:", filteredHospitals);

  if (filteredHospitals.length > 0) {
      filteredHospitals.forEach((hospital, index) => {
          const hospitalItem = document.createElement('div');
          hospitalItem.classList.add('hospital-item');
          hospitalItem.innerHTML = `
              <img src="${hospital.imageUrl}" alt="${hospital.name}">
              <div>
                  <h4>${hospital.name}</h4>
                  <p><strong>Address:</strong> ${hospital.address}</p>
                  <p><strong>Distance:</strong> ${hospital.distance} miles away</p>
                  <p><strong>Ratings:</strong> ${hospital.ratings} stars</p>
                  <p>${hospital.description}</p>
                  <a href="${hospital.profileLink}" target="_blank">View Profile</a>
              </div>
          `;
          resultsContainer.appendChild(hospitalItem);

          // Add a horizontal line except after the last item
          if (index < filteredHospitals.length - 1) {
              const hr = document.createElement('hr');
              hr.classList.add('hospital-divider');
              resultsContainer.appendChild(hr);
          }
      });
  } else {
      resultsContainer.textContent = 'No hospitals found.';
  }
}

